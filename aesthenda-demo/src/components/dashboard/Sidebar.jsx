const Sidebar = () => {
  const { currentUser, hasRole } = useAuth();
  const { currentTenant, getPrimaryColor } = useTenant();
  const location = useLocation();
  
  // Generate navigation items based on user role
  const getNavItems = () => {
    const items = [];
    
    // Dashboard home
    items.push({
      name: 'Dashboard',
      to: '/dashboard',
      icon: <CalendarIcon />,
      exact: true
    });
    
    // Appointment management (for all roles)
    items.push({
      name: 'Appointments',
      to: '/dashboard/appointments',
      icon: <AppointmentIcon />,
    });
    
    // Staff management - only for admin
    if (hasRole('salon_admin')) {
      items.push({
        name: 'Staff',
        to: '/dashboard/staff',
        icon: <UserIcon />,
      });
    }
    
    // Services management - admin and some staff
    if (hasRole(['salon_admin', 'salon_staff'])) {
      items.push({
        name: 'Services',
        to: '/dashboard/services',
        icon: <ServicesIcon />,
      });
    }
    
    // Clients - available to most roles
    items.push({
      name: 'Clients',
      to: '/dashboard/clients',
      icon: <ClientsIcon />,
    });
    
    // Analytics - admin only
    if (hasRole('salon_admin')) {
      items.push({
        name: 'Analytics',
        to: '/dashboard/analytics',
        icon: <AnalyticsIcon />,
      });
    }
    
    // Settings - admin only
    if (hasRole('salon_admin')) {
      items.push({
        name: 'Settings',
        to: '/dashboard/settings',
        icon: <SettingsIcon />,
      });
    }
    
    return items;
  };
  
  const navItems = getNavItems();
  const primaryColor = getPrimaryColor();
  
  return (
    <div className="h-full flex flex-col bg-white border-r border-gray-200">
      {/* Salon logo */}
      <div className="p-4 border-b border-gray-200">
        {currentTenant ? (
          <img 
            src={currentTenant.settings.branding.logoUrl} 
            alt={currentTenant.businessName}
            className="h-8 w-auto mx-auto"
          />
        ) : (
          <div className="h-8 w-full bg-gray-200 animate-pulse rounded"></div>
        )}
      </div>
      
      {/* Navigation */}
      <nav className="flex-1 px-2 py-4 space-y-1">
        {navItems.map((item) => {
          const isActive = item.exact
            ? location.pathname === item.to
            : location.pathname.startsWith(item.to);
            
          return (
            <Link
              key={item.name}
              to={item.to}
              className={`flex items-center px-2 py-2 text-sm font-medium rounded-md group ${
                isActive 
                  ? `bg-blue-50 text-blue-700` 
                  : `text-gray-600 hover:bg-gray-50 hover:text-gray-900`
              }`}
              style={isActive ? { backgroundColor: `${primaryColor}15`, color: primaryColor } : {}}
            >
              <div className="mr-3 flex-shrink-0">{item.icon}</div>
              {item.name}
            </Link>
          );
        })}
      </nav>
      
      {/* User info */}
      <div className="p-4 border-t border-gray-200">
        {currentUser && (
          <div className="flex items-center">
            <div className="w-8 h-8 rounded-full bg-gray-300 flex items-center justify-center">
              {currentUser.firstName.charAt(0) + currentUser.lastName.charAt(0)}
            </div>
            <div className="ml-3">
              <p className="text-sm font-medium text-gray-700">
                {currentUser.firstName} {currentUser.lastName}
              </p>
              <p className="text-xs text-gray-500 capitalize">
                {currentUser.role.replace('_', ' ')}
              </p>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

export default Sidebar;